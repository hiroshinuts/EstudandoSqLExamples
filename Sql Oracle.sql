
##########CRIANDO TABELA##########

CREATE TABLE COMPRAS (ID NUMBER PRIMARY KEY, VALOR NUMBER, DATA DATE, OBSERVACOES VARCHAR2(30), RECEBIDO CHAR CHECK (RECEBIDO IN (0,1)));

## CRIANDO SEQUENCIA DE ID;

CREATE SEQUENCE ID_SEQ;

## INSERINDO NA TABELA

